<?php
// 반복문 (이중 for 문)
// 1부터 9까지의 구구단 전체를 출력하는 코드

for ($i = 1; $i <= 9; $i++) {  // 외부 for문: 1단부터 9단까지 반복 (i는 1부터 9까지 증가)
  for ($j = 1; $j <= 9; $j++) {  // 내부 for문: 각 단의 1부터 9까지의 곱을 계산 (j는 1부터 9까지 증가)
    // $i와 $j의 곱을 계산한 후 출력
    echo "$i * $j = " . ($i * $j) . "<br>";  // 예: 1 * 1 = 1, 1 * 2 = 2, ...
  }

  echo "<br>";  // 각 단이 끝난 후 줄바꿈을 위해 <br> 출력 (각 단 사이에 줄 간격을 둠)
}
