<?php
// 구구단 출력하기 제목 출력
echo "구구단 출력하기" . "<br>";

// 단 설정 (1단 ~ 9단)
for ($dan = 1; $dan <= 9; $dan++) {
    // 단 제목 출력
    echo "### $dan 단 ###<br>";

    // 곱하는 숫자 (1부터 9까지)
    for ($i = 1; $i <= 9; $i++) {
        // 단과 숫자 곱 출력
        echo "$dan x $i = " . ($dan * $i) . "<br>";
    }

    // 각 단이 끝난 후 줄바꿈
    echo "<br>";
}
